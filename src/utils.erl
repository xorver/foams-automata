-module(utils).
-author("Tomasz Lichon").

%% API
-export([for/3, random_coordinates/1]).

%%%===================================================================
%%% API
%%%===================================================================

for(I, N, _Fun) when I > N -> ok;
for(I, N, Fun) ->
    Fun(I),
    for(I + 1, N, Fun).

random_coordinates(Max) ->
    {random:uniform(Max) - 1, random:uniform(Max) - 1}.

